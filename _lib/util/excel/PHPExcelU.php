			}
			if ($k == 6) { // ////////////////
				$a ["G"] = $sv;
			}
			if ($k == 7) { // ////////////////
				$a ["H"] = $sv;
			}
			if ($k == 8) { // ////////////////
				$a ["I"] = $sv;
			}
			if ($k == 9) { // ////////////////
				$a ["J"] = $sv;
			}
			if ($k == 10) { // ////////////////
				$a ["K"] = $sv;
			}
			if ($k == 11) { // ////////////////
				$a ["L"] = $sv;
			}
			if ($k == 12) { // ////////////////
				$a ["M"] = $sv;
			}
			if ($k == 13) { // ////////////////
				$a ["N"] = $sv;
			}
			if ($k == 14) { // ////////////////
				$a ["O"] = $sv;
			}
			if ($k == 15) { // ////////////////
				$a ["P"] = $sv;
			}
			if ($k == 16) { // ////////////////
				$a ["Q"] = $sv;
			}
			if ($k == 17) { // ////////////////
				$a ["R"] = $sv;
			}
			if ($k == 18) { // ////////////////
				$a ["S"] = $sv;
			}
			if ($k == 19) { // ////////////////
				$a ["T"] = $sv;
			}
			if ($k == 20) { // ////////////////
				$a ["U"] = $sv;
			}
			if ($k == 21) { // ////////////////
				$a ["V"] = $sv;
			}
			if ($k == 22) { // ////////////////
				$a ["W"] = $sv;
			}
			if ($k == 23) { // ////////////////
				$a ["X"] = $sv;
			}
			if ($k == 24) { // ////////////////
				$a ["Y"] = $sv;
			}
			if ($k == 25) { // ////////////////
				$a ["Z"] = $sv;
			}
			
			
			
			if ($k == 26) { // ////////////////
				$a ["AA"] = $sv;
			}
			if ($k == 27) { // ////////////////
				$a ["AB"] = $sv;
			}
			if ($k == 28) { // ////////////////
				$a ["AC"] = $sv;
			}
			if ($k == 29) { // ////////////////
				$a ["AD"] = $sv;
			}
			if ($k == 30) { // ////////////////
				$a ["AE"] = $sv;
			}
			if ($k == 31) { // ////////////////
